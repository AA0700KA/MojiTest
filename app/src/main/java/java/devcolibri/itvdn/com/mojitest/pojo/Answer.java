package java.devcolibri.itvdn.com.mojitest.pojo;

public class Answer {

    private String answer;
    private int data;

    public Answer(String answer, int data) {
        this.answer = answer;
        this.data = data;
    }

    public String getAnswer() {
        return answer;
    }

    public void setAnswer(String answer) {
        this.answer = answer;
    }

    public int getData() {
        return data;
    }

    public void setData(int data) {
        this.data = data;
    }

}
